'''Listas
Coleccion ordenada y mutable que almacena cualquier tipo de datos '''

#definir una lista

miLista = [5,10,15, "hello"] 

print (miLista)

#Acceder a una lista
print(miLista[3])

#agregar un elemento a la lista
miLista.append (24)
print (miLista)

#remover un elemento de la lista 
miLista.remove (10)
print (miLista)

#contador
print(len(miLista))
